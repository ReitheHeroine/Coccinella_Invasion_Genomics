# purpose:
#   Manhattan-scatter diagnostic, chr-1 follow-up between Task 3 and Task 4.
#   Task 3 found that chr 1 (NC_058189.1) dominates per-chromosome outlier
#   density in every one of the 6 pairwise comparisons, including the
#   among-invasive ones where there has been minimal time for parallel
#   locus-specific selection. That cross-comparison universality is the
#   signal that warrants checking before any SNP-resolution clustering
#   analysis (Task 4) is meaningful. This script runs four checks to
#   distinguish "real chromosomal selection target" from "chr-1 specific
#   technical artifact" hypotheses:
#
#     Check 1. SNP density by chromosome, comparing PLINK QC universe
#              (pre-FST-filter) against the FST-callable universe
#              (post-NA-drop) and against length. Chr 1 outlying high in
#              SNPs/Mb at either level would point at variant-calling
#              hotspots (paralog mismapping, repeat-region calls, etc.).
#     Check 2. Spatial distribution of chr-1 outliers in 1 Mb bins.
#              Distinguishes "chromosome-wide elevation" (consistent with
#              broad biological cause) from "narrow peak" (specific
#              selection target) from "centromere/telomere clustering"
#              (mappability artifact).
#     Check 3. F_ST magnitude of chr-1 outliers vs. other-chromosome
#              outliers. If chr-1 outliers sit at systematically lower
#              F_ST than other-chrom outliers, chr 1 is over-represented
#              just because it has lots of borderline-FST SNPs (artifact
#              of the genome-wide percentile threshold). If chr-1
#              outliers match or exceed other-chrom outliers in F_ST,
#              the chr-1 signal is driven by genuinely high-FST SNPs.
#     Check 4. MAF distribution of chr-1 SNPs vs. other chromosomes
#              (genome-wide PLINK universe, not just outliers). Chr-1
#              MAF inflated relative to other chromosomes is a hallmark
#              of paralog-mismapping or repeat-region SNP-calling
#              artifacts (false heterozygotes inflate MAF toward 0.5).
#
# inputs:
#   - results/fst/{COMP}/fst_genomewide/{CHROM}.filtered.weir.fst
#       Per-SNP F_ST. NA-drop = post-filter for check 1; values for check 3.
#   - results/per_snp_selection_scan/outliers_1pct_chifiltered.tsv
#       Outlier set used for checks 2 and 3.
#   - data/plink/ladybug_snps.bim
#       PLINK QC universe (pre-FST-filter SNP count for check 1).
#   - <generated by this script via plink --freq>
#       /tmp/ladybug_maf.frq
#       Genome-wide MAF for check 4. The script regenerates it if absent.
#   - VCF header contigs (for chromosome lengths). Read once via zcat.
#
# outputs:
#   - figures/diagnostic/chr1_followup/
#       check1_snp_density_by_chrom.png
#       check2_chr1_spatial_distribution.png
#       check3_fst_chr1_vs_other.png
#       check4_maf_chr1_vs_other.png
#       summary_panel.png         (4-panel combined view)
#   - results/diagnostic/chr1_followup_summary.tsv
#       One row per chromosome with: length_mb, plink_snps, plink_snps_per_mb,
#       fst_snps_<comp>, fst_snps_per_mb_<comp>, mean_maf, median_maf.
#   - results/diagnostic/chr1_followup_check3.tsv
#       Per-comparison F_ST stats split by chr-1 vs other-chrom outliers.
#   - results/diagnostic/chr1_followup_run.log
#
# usage example:
#   Rscript scripts/diagnostic_chr1_followup.R

